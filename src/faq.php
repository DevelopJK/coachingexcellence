                            <h2>Co mi koučování může přinést?</h2>

<p>
Přinést vám může především hlubší poznání sebe sama. Nejčastěji klienti pociťují větší míru

spokojenosti, radosti, klidu, pocitu, že nejsou tolik ve stres. Daří se jim lépe zvládat své vztah, lépe se 

rozhodují, získávají své principy, které mohou uplatnit v soukromém i profesním životě.</p>
<!--<hr width="97%" noshade color="gray">-->

<h2>Kolik sezení mám absolvovat?</h2>

<p>Záleží na tom, jaké potřeby koučování máte. Standardně se spolupráce pohybuje v rozsahu 4-10

 sezení. Delší forma spolupráce je vhodná např. pro doprovázení ve složitém období života, 

při uskutečňování důležitých změn, pro vedení na cestě osobního rozvoje apod. Někdy může být 

spolupráce naopak velmi rychlá, kdy si potřebujete ujasnit konkrétní problém či dospějete k řešení 

rovnou.</p>
<!--<hr width="97%" noshade color="gray">-->

<h2>Jak často ke kouči docházet?</h2>
<p>Záleží na tom, co Vám vyhovuje, někomu vyhovuje sezení 1x týdně,jinému 1x 14 dní, případně 1x

měsíc.</p>

  
<!--<hr width="97%" noshade color="gray">-->

<h2>Co si mám připravit na první koučovací sezení?</h2>
<p>Předem si nemusíte nic připravovat. Přicházíte s tématem, potřebou změny, nespokojeností, zkrátka s

tím, co potřebujete vyřešit.</p>
<!--<hr width="97%" noshade color="gray">-->

 <h2>Čeho všeho mohu koučováním dosáhnout?</h2>
<p>Jediným, kdo nám klade limity, jsme my sami.</p>
<!--<hr width="97%" noshade color="gray">-->

 <h2>Nemám na koučování čas, co s tím?</h2>
<p>Zřejmě patříte k většině naší populace. Času máme všichni stejně, je jen na nás čemu ho budeme 

věnovat. Neustále svádíme souboj s časem, lze přestat bojovat, začít více plynout, plánovat a zacházet 

s časem tak, aby byl naším spojencem. Koučováním můžete spoustu času ušetřit, když objevíte své 

unikátní zacházení s časem.</p>
<!--<hr width="97%" noshade color="gray">-->

 <h2>Jaký je rozdíl mezi koučováním osobně a přes telefon/skype?</h2>
<p>Tady je to velmi individuální, záleží na klientovi. Někdo upřednostňuje osobní kontakt. Koučování přes

telefon nebo skype výrazně šetří náklady, cena koučování je pak asi o třetinu levnější. Znám mnoho 

lidí, kteří se nejprve koučovat "na dálku" zdráhali, a pak seznali, že jim to velmi vyhovuje. Obojí lze i 

kombinovat, když např. preferujete koučování osobně a na služební cestě máte sezení přes telefon/

skype.</p>

<h2>Můžeme se sejít jinde než v koučovně nebo v kanceláři?</h2>
<p>Koučování není omezené prostorem, koučovat lze v kavárně, v park, na procházce. Důležité je, kde se

dobře cítíte, jestli potřebujete k soustředění se ticho nebo tichý ruch na pozadí. Záleží na dohodě obou 

stran.</p>




